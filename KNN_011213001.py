import math


def classify_colors(training_colors, test_color, k):
    distance = []
    for group in training_colors:
        for rgb_values in training_colors[group]:
            euclidean_distance = math.sqrt(
                (rgb_values[0] - test_color[0]) ** 2 +
                (rgb_values[1] - test_color[1]) ** 2 +
                (rgb_values[2] - test_color[2]) ** 2)
            distance.append((euclidean_distance, group))

    distance = sorted(distance)[:k]
    freq = [0, 0, 0]
    for d in distance:
        freq[d[1]] += 1
    max_freq = max(freq)
    max_freq_index = freq.index(max_freq)
    if max_freq_index == 0:
        print('Red')
    elif max_freq_index == 1:
        print('Green')
    else:
        print('Blue')


